package com.afrigis.services.search.extension.postalcode;

/**
 * <p>
 * Represents CommercialActivities obtained from AfriGIS Postal Code Service
 * call
 * </p>
 *
 * @author Takalani
 */
public class CommercialActivities {

    private int Automotive;

    public int getAutomotive() {
        return this.Automotive;
    }

    public void setAutomotive(int Automotive) {
        this.Automotive = Automotive;
    }

    private int Commercial;

    public int getCommercial() {
        return this.Commercial;
    }

    public void setCommercial(int Commercial) {
        this.Commercial = Commercial;
    }

    private int Entertainment;

    public int getEntertainment() {
        return this.Entertainment;
    }

    public void setEntertainment(int Entertainment) {
        this.Entertainment = Entertainment;
    }

    private int FillingStations;

    public int getFillingStations() {
        return this.FillingStations;
    }

    public void setFillingStations(int FillingStations) {
        this.FillingStations = FillingStations;
    }

    private int Medical;

    public int getMedical() {
        return this.Medical;
    }

    public void setMedical(int Medical) {
        this.Medical = Medical;
    }

    private int Services;

    public int getServices() {
        return this.Services;
    }

    public void setServices(int Services) {
        this.Services = Services;
    }

    private int Stores;

    public int getStores() {
        return this.Stores;
    }

    public void setStores(int Stores) {
        this.Stores = Stores;
    }

    private int Tourism;

    public int getTourism() {
        return this.Tourism;
    }

    public void setTourism(int Tourism) {
        this.Tourism = Tourism;
    }

    private int Transportation;

    public int getTransportation() {
        return this.Transportation;
    }

    public void setTransportation(int Transportation) {
        this.Transportation = Transportation;
    }
}
